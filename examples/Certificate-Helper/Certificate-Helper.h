/*
=== CERTIFICATE VALIDATION UTILITY ===

Here's a simple function to validate if a certificate appears to be properly formatted:
*/

bool validateCertificate(const char* cert) {
    if (cert == NULL) return false;
    
    // Check for proper PEM headers
    if (strstr(cert, "-----BEGIN CERTIFICATE-----") == NULL) {
        Serial.println("Error: Missing BEGIN CERTIFICATE header");
        return false;
    }
    
    if (strstr(cert, "-----END CERTIFICATE-----") == NULL) {
        Serial.println("Error: Missing END CERTIFICATE header");
        return false;
    }
    
    // Basic length check (certificates are typically quite long)
    if (strlen(cert) < 500) {
        Serial.println("Warning: Certificate seems unusually short");
        return false;
    }
    
    Serial.println("Certificate format appears valid");
    return true;
}

